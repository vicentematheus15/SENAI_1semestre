let num = [5, 8, 9, 3]

console.log(`Nosso array é ${num}`)

num.push(7) // adiciona mais um elemento no final da array

console.log(`Agora o array é ${num}`)

num.length //tamanho da array
console.log(`O vetor tem ${num.length} posições`)

console.log(`O primeiro valor do vetor é ${num[0]}`)

num.sort() //organiza em ordem
console.log(`O vetor após ser organizado com .sort ficou assim ${num}`)